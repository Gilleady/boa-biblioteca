    public void listar() {
        DAO dao = new DAO();
        if (dao.conectar()) {
            List<Livro> lista = dao.listarLivros();
            DefaultTableModel dados = (DefaultTableModel) tblLivros.getModel();
            dados.setNumRows(0);
            for (Livro l : lista) {
                dados.addRow(new Object[]{
                    l.getISBN(),
                    l.getTitulo(),
                    l.getAutor(),
                    l.getCategoria(),
                    l.getAno(),
                    l.getEditora()
                });
            }
            dao.desconectar();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao conectar com o banco de dados");
        }
    }

